export class UserModel {

    /**
     * User's first name
     */
    public FirstName: string;

    /**
     * User icon (avatar)
     */
    public Icon?: string;

    /**
     * User id
     */
    public Id: number;

    /**
     * Store logged in
     */
    public IsLoggedIn: boolean;

    /**
     * User's last name
     */
    public LastName: string;

    /**
     * User message
     */
    public Message: string;

    /**
     * User password
     */
    public Password: string;

    /**
     * User Role
     */
    public Role: string;

    /**
     * User name
     */
    public Username: string;
}
